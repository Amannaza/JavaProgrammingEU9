package day05_Concatenation;

public class Calculator {
    public static void main(String[] args) {
        int firstNumber = 100;
        int secondNumber = 50;
        int addition = firstNumber + secondNumber;
        int subtraction = firstNumber - secondNumber;
        int multiplication = firstNumber * secondNumber;
        int subtraction1 = firstNumber / secondNumber;
        //100+50=150
        System.out.println("Addition----->" + firstNumber + " + " + secondNumber + " = " + addition);

        //100-50=50
        System.out.println("Subtracrion---->" + firstNumber + " - " + secondNumber + " = " + subtraction);

        //100*50=500
        System.out.println("Multiplication--->" + firstNumber + " + " + secondNumber + " = " + multiplication);

        //100รท50=2
        System.out.println("Subtraction--->" + firstNumber + " รท " + secondNumber + " = " + subtraction1);


    }

}
