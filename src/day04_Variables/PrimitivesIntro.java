package day04_Variables;

public class PrimitivesIntro {
    public static void main(String[] args) {
        char ch1 = '#';
        System.out.println("ch1 = " + ch1);

        char ch2 = 35;
        System.out.println("ch2 = " + ch2);

        char ch3 = 1389;
        System.out.println("ch3 = " + ch3);

        char gender = 'F';

        char grade = 'F';

        char YesNo = 'Y';

//char ch5='av'---char is only for single chracter

        boolean isEmployed = true;
        boolean isMarrried = false;

        boolean result = 100 > 300;

        System.out.println("isEmployed = " + isEmployed);
        System.out.println("isMarrried = " + isMarrried);
        System.out.println("result = " + result);

        String name = "Wooden Spoon";
        String city = "New York";
        String state = "Virginia";
        String country = "USA";
        System.out.println("name = " + name);
        System.out.println("city = " + city);
        System.out.println("state = " + state);
        System.out.println("country = " + country);
// We can only use "$" and "_" signs for variables
        String first_name="Muhtar";
        System.out.println("first_name = " + first_name);

    }
}
