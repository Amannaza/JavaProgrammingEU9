package day17_While_DoWhile;

public class IntroLoopWhile {
    public static void main(String[] args) {

        if (true){
            System.out.println("Wooden Spoon");  //one for one time
        }
        System.out.println("-----------------------");

        while (true){
            System.out.println("Wooden Spoon"); //boolean for multiple time
        }

    }
}
